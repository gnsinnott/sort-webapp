    Weight = IntegerField('Weight', validators=[Optional(strip_whitespace=True)])
    WrongPart = IntegerField('WrongPart', validators=[Optional(strip_whitespace=True)])
    submit = SubmitField('Submit', validators=[Optional(strip_whitespace=True)])

