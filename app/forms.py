    Blisters = IntegerField('Blisters', validators=[Optional(strip_whitespace=True)])
    BrokenCore = IntegerField('BrokenCore', validators=[Optional(strip_whitespace=True)])
    Buffing = IntegerField('Buffing', validators=[Optional(strip_whitespace=True)])
    Contamination = IntegerField('Contamination', validators=[Optional(strip_whitespace=True)])
    DamagedDie = IntegerField('DamagedDie', validators=[Optional(strip_whitespace=True)])
    Debris = IntegerField('Debris', validators=[Optional(strip_whitespace=True)])
    Dimensional = IntegerField('Dimensional', validators=[Optional(strip_whitespace=True)])
    Flash = IntegerField('Flash', validators=[Optional(strip_whitespace=True)])
    GateVestige = IntegerField('GateVestige', validators=[Optional(strip_whitespace=True)])
    HeatSinks = IntegerField('HeatSinks', validators=[Optional(strip_whitespace=True)])
    EjectorPinLength = IntegerField('EjectorPinLength', validators=[Optional(strip_whitespace=True)])
    Lamination = IntegerField('Lamination', validators=[Optional(strip_whitespace=True)])
    LeakTest = IntegerField('LeakTest', validators=[Optional(strip_whitespace=True)])
    MixedParts = IntegerField('MixedParts', validators=[Optional(strip_whitespace=True)])
    Other = IntegerField('Other', validators=[Optional(strip_whitespace=True)])
    PartDamage = IntegerField('PartDamage', validators=[Optional(strip_whitespace=True)])
    PartsNotTapped = IntegerField('PartsNotTapped', validators=[Optional(strip_whitespace=True)])
    PartsOnGates = IntegerField('PartsOnGates', validators=[Optional(strip_whitespace=True)])
    Plating = IntegerField('Plating', validators=[Optional(strip_whitespace=True)])
